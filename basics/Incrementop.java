package basics;

public class Incrementop { public static void main(String[]args) {
	int i = 0; 
	int j = 10;
	int z = 5;
	System.out.println(i++ + ++i + ++i + i++ );
	System.out.println(i);
	System.out.println(++j + j++ + j++ + ++j);
	System.out.println(j);
	System.out.println(++z + z++ + ++z + ++z);
	
}                        

}
