package basics;

public class Math_class { public static void main(String []args) {
	System.out.println("Mathematics operation");
	System.out.println("Max:"+Math.max(2,5));
	System.out.println("Min:"+Math.min(2,5));
	System.out.println("absolute:"+Math.abs(3.7));
	System.out.println("exp:"+Math.exp(10));
	System.out.println("random no.:"+Math.random());
	System.out.println("Square root:"+Math.sqrt(5));
	
}


}
